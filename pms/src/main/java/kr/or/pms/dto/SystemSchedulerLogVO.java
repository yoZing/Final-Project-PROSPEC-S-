package kr.or.pms.dto;

import java.util.Date;

public class SystemSchedulerLogVO {
	private int sysSchedNo;
	private String nm;
	private String info;
	private Date bgnDt;
	private Date endDt;
	private String takedTime;
	private String resultCode;
	
	public int getSysSchedNo() {
		return sysSchedNo;
	}
	public void setSysSchedNo(int sysSchedNo) {
		this.sysSchedNo = sysSchedNo;
	}
	public String getNm() {
		return nm;
	}
	public void setNm(String nm) {
		this.nm = nm;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public Date getBgnDt() {
		return bgnDt;
	}
	public void setBgnDt(Date bgnDt) {
		this.bgnDt = bgnDt;
	}
	public Date getEndDt() {
		return endDt;
	}
	public void setEndDt(Date endDt) {
		this.endDt = endDt;
	}
	public String getTakedTime() {
		return takedTime;
	}
	public void setTakedTime(String takedTime) {
		this.takedTime = takedTime;
	}
	public String getResultCode() {
		return resultCode;
	}
	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}
}
