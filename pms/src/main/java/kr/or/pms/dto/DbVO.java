package kr.or.pms.dto;

public class DbVO {
	
	private int dbNo;
	private String dbNm;
	private int dbcnNo;
	
	public int getDbNo() {
		return dbNo;
	}
	public void setDbNo(int dbNo) {
		this.dbNo = dbNo;
	}
	public String getDbNm() {
		return dbNm;
	}
	public void setDbNm(String dbNm) {
		this.dbNm = dbNm;
	}
	public int getDbcnNo() {
		return dbcnNo;
	}
	public void setDbcnNo(int dbcnNo) {
		this.dbcnNo = dbcnNo;
	}

}
