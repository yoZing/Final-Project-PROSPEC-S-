package kr.or.pms.dto;

public class HtmlTempVO {
	
	private String tempNm;
	private String position;
	private String tempOrder;
	private String content;
	
	
	public String getTempNm() {
		return tempNm;
	}
	public void setTempNm(String tempNm) {
		this.tempNm = tempNm;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getTempOrder() {
		return tempOrder;
	}
	public void setTempOrder(String tempOrder) {
		this.tempOrder = tempOrder;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}

	
}
